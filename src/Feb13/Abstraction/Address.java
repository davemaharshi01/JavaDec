package Feb13.Abstraction;

public class Address {
    String streetNumber;
    String city;
    String country;
    String postalcode;

    public Address(String streetNumber, String city, String country, String postalcode) {
        this.streetNumber = streetNumber;
        this.city = city;
        this.country = country;
        this.postalcode = postalcode;
    }
}
