package Feb13.Encapsulation;

public class LoginUserW {

    int id;
    String userName;

    public LoginUserW(int id, String userName) {
        this.id = id;
        this.userName = userName;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

}
